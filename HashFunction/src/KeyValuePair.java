
public class KeyValuePair1{
	int key;
	String value;
	KeyValuePair1( int key , String value) {
		this.key = key;
		this.value = value;
	}
	
	public int GetKey() {
	return key;
	}
	
	public String GetValue() {
		return value;
	}
	
	public void SetValue(String value) {
		this.value=value;
	}
	

}
